    local CACHE_DIR="data/hf-cache"
    mkdir -p "$CACHE_DIR/hub"

    # Проверка: если модель уже скачана — пропускаем
    local MODEL_DIR="$CACHE_DIR/hub/models--Systran--faster-whisper-medium"
    if [[ -d "$MODEL_DIR" && -f "$MODEL_DIR/snapshots/"*"/model.bin" ]]; then
        info "Модель Whisper уже скачана."
        return 0
    fi

    # Скачивание через huggingface_hub (Python)
    info "Скачиваю ~1.5 ГБ — это может занять несколько минут..."
    python3 -c "
from huggingface_hub import snapshot_download
import os, sys

os.environ['HF_HUB_DOWNLOAD_TIMEOUT'] = '600'
try:
    path = snapshot_download(
        'Systran/faster-whisper-medium',
        cache_dir='$CACHE_DIR',
        ignore_patterns=['*.h5', '*.ot', '*.msgpack']
    )
    print(f'OK: модель скачана в {path}')
except Exception as e:
    print(f'ERROR: {e}')
    sys.exit(1)
" && info "Модель Whisper успешно скачана." || warn "Не удалось скачать модель Whisper. ASR будет недоступен."
}

# ── Сборка и запуск ──
build_and_launch() {
    local PROFILE=""
    [[ "$WITH_IMAGE_GEN" == "true" ]] && PROFILE="$PROFILE --profile with-image-gen"
    [[ "$WITH_VOICE" == "true" ]] && PROFILE="$PROFILE --profile with-voice"
    [[ "$WITH_RAG" == "true" ]]    && PROFILE="$PROFILE --profile with-rag"
    [[ "$WITH_VIDEO" == "true" ]]  && PROFILE="$PROFILE --profile with-video"
    [[ "$WITH_SLM" == "true" ]]    && PROFILE="$PROFILE --profile with-slm"
    [[ "$WITH_SEARCH" == "true" ]] && PROFILE="$PROFILE --profile with-search"

    COMPOSE_FILE="docker-compose.gpu.yml"
    info "Режим GPU — используется GPU compose файл."

    # Определяем уровень VRAM
    local VRAM_MB=0
    if command -v nvidia-smi &>/dev/null; then
        VRAM_MB=$(nvidia-smi --query-gpu=memory.total --format=csv,noheader,nounits 2>/dev/null | head -1 | tr -d ' ')
    fi
    if [[ "$VRAM_MB" -ge 16000 ]]; then
        info "VRAM: ${VRAM_MB}MB (уровень: 16GB+) — полная производительность (gpt-oss-20b рассуждения)"
    elif [[ "$VRAM_MB" -ge 12000 ]]; then
        info "VRAM: ${VRAM_MB}MB (уровень: 12GB) — хорошая посадка (Gemma 4 E4B рассуждения)"
    elif [[ "$VRAM_MB" -ge 8000 ]]; then
        info "VRAM: ${VRAM_MB}MB (уровень: 8GB) — тесно (Gemma 4 E4B рассуждения)"
    else
        warn "VRAM: ${VRAM_MB}MB — менее 8GB минимума. Производительность будет сильно ограничена."
    fi

    # Удаляем старые контейнеры во избежание конфликтов
    info "Останавливаю старые контейнеры (если есть)..."
    docker compose -f "$COMPOSE_FILE" down --remove-orphans 2>/dev/null || true

    info "Собираю Docker-образы..."
    docker compose -f "$COMPOSE_FILE" $PROFILE build

    info "Запускаю сервисы..."
    docker compose -f "$COMPOSE_FILE" $PROFILE up -d

    info "Ожидаю запуск сервисов..."
    sleep 10

    local STATUS
    STATUS=$(curl -s http://localhost:5000/health 2>/dev/null | python3 -c "import sys,json; print(json.load(sys.stdin).get('status','unknown'))" 2>/dev/null || echo "недоступен")
    if [[ "$STATUS" == "ok" ]]; then
        info "FLAI v9.0 запущен! Откройте http://localhost:5000 в браузере."
    else
        warn "Проверка здоровья: $STATUS — проверьте 'docker compose logs' для деталей."
    fi
}

