# ── Цвета ──
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'; NC='\033[0m'
info()  { echo -e "${GREEN}[INFO]${NC} $1"; }
warn()  { echo -e "${YELLOW}[WARN]${NC} $1"; }
error() { echo -e "${RED}[ERROR]${NC} $1"; exit 1; }

# ── Проверка зависимостей ──
check_prereqs() {
    info "Проверка зависимостей..."
    command -v docker &>/dev/null     || error "Docker не установлен. Установите Docker."
    command -v docker compose &>/dev/null || error "Плагин Docker Compose не установлен."
    command -v git &>/dev/null        || error "Git не установлен."
    if ! command -v nvidia-smi &>/dev/null; then
        warn "Видеокарта NVIDIA не обнаружена — ускорение на GPU будет недоступно."
    fi
    if docker info 2>/dev/null | grep -q "Runtimes.*nvidia"; then
        info "NVIDIA Docker runtime обнаружен."
    elif command -v nvidia-smi &>/dev/null; then
        warn "nvidia-smi найден, но nvidia-container-runtime не обнаружен в Docker."
    fi
    info "Зависимости проверены."
}

# ── Настройка окружения ──
setup_env() {
    if [[ -f .env ]]; then
        warn ".env уже существует — пропускаем настройку."
        return
    fi
    info "Создаю .env из .env.example..."
    cp .env.example .env
    SECRET=$(python3 -c "import secrets; print(secrets.token_hex(32))" 2>/dev/null || openssl rand -hex 32)
    sed -i "s/SECRET_KEY=.*/SECRET_KEY=$SECRET/" .env
    info ".env создан. Отредактируйте его для настройки моделей, URL и параметров."
}

# ── Директории данных ──
setup_dirs() {
    info "Создаю директории данных..."
    mkdir -p data/uploads data/documents data/db_backups data/slm
    chown -R 1000:1000 data 2>/dev/null || warn "Не удалось изменить владельца data/ — выполните с sudo."
}

# ── Проверка .env ──
validate_env() {
    if [[ ! -f .env ]]; then
        warn ".env не найден — сначала запустите deploy.sh."
        return
    fi
    local errors=0
    grep -qP '^SECRET_KEY=\S+' .env 2>/dev/null || { warn "SECRET_KEY не задан в .env"; errors=1; }
    grep -qP '^DATABASE_URL=\S+' .env 2>/dev/null || { warn "DATABASE_URL не задан в .env"; errors=1; }
    [[ "$errors" -eq 1 ]] && warn "Отредактируйте .env перед продолжением."
}

# ── Генерация минимального конфига llama-swap ──
generate_llama_swap_config() {
    local CONFIG_DIR="llama-swap-config"
    mkdir -p "$CONFIG_DIR"

    if [[ -f "$CONFIG_DIR/llama-swap.yaml" ]]; then
        return
    fi

    info "Генерирую минимальный конфиг llama-swap..."

    local CHAT_MODEL="Qwen3-4B-Instruct-2507-Q4_0"
    if command -v nvidia-smi &>/dev/null; then
        local GPU_NAME
        GPU_NAME=$(nvidia-smi --query-gpu=name --format=csv,noheader,nounits 2>/dev/null | head -1 | tr -d ' ')
        if [[ "${GPU_NAME,,}" =~ (rtx 5060|rtx 5070|rtx 5080|rtx 5090|blackwell|b100|b200|gb200|gb10) ]]; then
            CHAT_MODEL="Qwen3-4B-Instruct-2507-MXFP4_MOE"
        fi
    fi

    cat > "$CONFIG_DIR/llama-swap.yaml" << YAML
# Auto-generated by deploy.sh — replaced by web app on startup
default_model: chat
models:
  - model: /models/${CHAT_MODEL}.gguf
    name: chat
    ctx_size: 16384
YAML
}

# ── Вспомогательная функция скачивания моделей ──
HF_DOWNLOAD() {
    local repo="$1" file="$2" dest="$3"
    local url="https://huggingface.co/$repo/resolve/main/$file"
    info "Скачиваю $file из HuggingFace..."
    local dir="$(dirname "$dest")"
    mkdir -p "$dir"
    
    # Retry download up to 3 times
    for attempt in 1 2 3; do
        curl -L --progress-bar -o "$dest" "$url"
        
        # Check if file is valid (more than 1KB)
        local size=$(stat -c%s "$dest" 2>/dev/null || echo "0")
        if [[ "$size" -gt 1024 ]]; then
            info "Успешно скачано: $file ($size bytes)"
            return 0
        else
            warn "Попытка $attempt не удалась, повторяю..."
            rm -f "$dest"
            sleep 2
        fi
    done
    error "Не удалось скачать $file после 3 попыток"
}

# ── Модели llama.cpp ──
download_llamacpp_models() {
    info "Скачиваю модели llama.cpp..."
    local MODEL_DIR="services/llamacpp/models"
    local VRAM_MB=0
    local IS_BLACKWELL=false
    local CHAT_MODEL=""
    local REASONING_MODEL=""

    if command -v nvidia-smi &>/dev/null; then
        VRAM_MB=$(nvidia-smi --query-gpu=memory.total --format=csv,noheader,nounits 2>/dev/null | head -1 | tr -d ' ')
    fi

    # Определяем архитектуру GPU — MXFP4 только на Blackwell (аппаратные FP4 тензорные ядра)
    if command -v nvidia-smi &>/dev/null; then
        local GPU_NAME
        GPU_NAME=$(nvidia-smi --query-gpu=name --format=csv,noheader,nounits 2>/dev/null | head -1 | tr -d ' ')
        if [[ "${GPU_NAME,,}" =~ (rtx 5060|rtx 5070|rtx 5080|rtx 5090|blackwell|b100|b200|gb200|gb10) ]]; then
            IS_BLACKWELL=true
        fi
    fi

    if [[ "$IS_BLACKWELL" == true ]]; then
        CHAT_MODEL="Qwen3-4B-Instruct-2507-MXFP4_MOE"
        REASONING_MODEL="gpt-oss-20b-mxfp4"
    else
        CHAT_MODEL="Qwen3-4B-Instruct-2507-Q4_0"
        REASONING_MODEL="gpt-oss-20b-Q4_K_M"
    fi

    # Чат-модель (роутер) — квантизация зависит от архитектуры GPU
    if [[ ! -f "$MODEL_DIR/${CHAT_MODEL}.gguf" ]]; then
        info "Скачиваю ${CHAT_MODEL}.gguf (чат/роутер)..."
        HF_DOWNLOAD "unsloth/Qwen3-4B-Instruct-2507-GGUF" \
            "${CHAT_MODEL}.gguf" \
            "$MODEL_DIR/${CHAT_MODEL}.gguf"
    else
        warn "${CHAT_MODEL}.gguf уже есть — пропускаю."
    fi

    # Модель рассуждений — выбор по VRAM и архитектуре
    if [[ "$VRAM_MB" -ge 16000 ]]; then
        # 16GB+ — полная модель (квантизация зависит от архитектуры)
        if [[ ! -f "$MODEL_DIR/${REASONING_MODEL}.gguf" ]]; then
            info "Скачиваю ${REASONING_MODEL}.gguf (рассуждения, уровень 16GB+)..."
            HF_DOWNLOAD "unsloth/gpt-oss-20b-GGUF" \
                "${REASONING_MODEL}.gguf" \
                "$MODEL_DIR/${REASONING_MODEL}.gguf"
        else
            warn "${REASONING_MODEL}.gguf уже есть — пропускаю."
        fi
    else
        # 12GB и 8GB — Gemma 4 E4B Q4_0 (~4,8 ГБ)
        if [[ ! -f "$MODEL_DIR/gemma-4-E4B-it-Q4_0.gguf" ]]; then
            info "Скачиваю gemma-4-E4B-it-Q4_0.gguf (рассуждения)..."
            HF_DOWNLOAD "unsloth/gemma-4-E4B-it-GGUF" \
                "gemma-4-E4B-it-Q4_0.gguf" \
                "$MODEL_DIR/gemma-4-E4B-it-Q4_0.gguf"
        else
            warn "gemma-4-E4B-it-Q4_0.gguf уже есть — пропускаю."
        fi
    fi

    # Мультимодальная модель — выбор по VRAM
    if [[ "$VRAM_MB" -ge 12000 ]]; then
        if [[ ! -d "$MODEL_DIR/Qwen3VL-8B-Instruct-Q4_K_M" ]]; then
            info "Скачиваю Qwen3VL-8B-Instruct-Q4_K_M (мультимодальная, уровень 12GB+)..."
            mkdir -p "$MODEL_DIR/Qwen3VL-8B-Instruct-Q4_K_M"
            HF_DOWNLOAD "Qwen/Qwen3-VL-8B-Instruct-GGUF" \
                "Qwen3VL-8B-Instruct-Q4_K_M.gguf" \
                "$MODEL_DIR/Qwen3VL-8B-Instruct-Q4_K_M/Qwen3VL-8B-Instruct-Q4_K_M.gguf"
            HF_DOWNLOAD "Qwen/Qwen3-VL-8B-Instruct-GGUF" \
                "mmproj-Qwen3VL-8B-Instruct-F16.gguf" \
                "$MODEL_DIR/Qwen3VL-8B-Instruct-Q4_K_M/mmproj-F16.gguf"
        else
            warn "Qwen3VL-8B-Instruct-Q4_K_M уже есть — пропускаю."
        fi
    elif [[ "$VRAM_MB" -ge 8000 ]]; then
        info "VRAM ${VRAM_MB}MB: скачиваю лёгкую мультимодальную (Qwen3VL-4B)..."
        if [[ ! -d "$MODEL_DIR/Qwen3VL-4B-Instruct-Q4_K_M" ]]; then
            mkdir -p "$MODEL_DIR/Qwen3VL-4B-Instruct-Q4_K_M"
            HF_DOWNLOAD "Qwen/Qwen3-VL-4B-Instruct-GGUF" \
                "Qwen3VL-4B-Instruct-Q4_K_M.gguf" \
